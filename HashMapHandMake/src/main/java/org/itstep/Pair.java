package org.itstep;

public class Pair {

	private final Integer key;
	private final Long value;
	
	public Pair(Integer key, Long value) {
		super();
		this.key = key;
		this.value = value;
	}

	public Integer getKey() {
		return key;
	}

	public Long getValue() {
		return value;
	}
	
	
}
